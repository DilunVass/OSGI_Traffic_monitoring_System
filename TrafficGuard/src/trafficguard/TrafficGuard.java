package trafficguard;

public class TrafficGuard {
	
	
	public int camID;
	public String camView;
	
	public int sensorSetID;
	public String sensorData;
	
	
	
	public int getCamID() {
		return camID;
	}
	public void setCamID(int camID) {
		this.camID = camID;
	}
	public String getCamView() {
		return camView;
	}
	public void setCamView(String camView) {
		this.camView = camView;
	}
	public int getSensorSetID() {
		return sensorSetID;
	}
	public void setSensorSetID(int sensorSetID) {
		this.sensorSetID = sensorSetID;
	}
	public String getSensorData() {
		return sensorData;
	}
	public void setSensorData(String sensorData) {
		this.sensorData = sensorData;
	}
	
	
	
}
