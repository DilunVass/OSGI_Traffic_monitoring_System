package trafficlightsystem;

public class TrafficLightSyastem {

    public int junctureID;
    public int roadID;
    public boolean roadStatus;
    
    
	public int getJunctureID() {
		return junctureID;
	}
	public void setJunctureID(int junctureID) {
		this.junctureID = junctureID;
	}
	public int getRoadID() {
		return roadID;
	}
	public void setRoadID(int roadID) {
		this.roadID = roadID;
	}
	public boolean isRoadStatus() {
		return roadStatus;
	}
	public void setRoadStatus(boolean roadStatus) {
		this.roadStatus = roadStatus;
	}

    
}
